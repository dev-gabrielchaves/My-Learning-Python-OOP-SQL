"""Technical Terms: Higher Order Functions and First-Class Functions
Academically, the terms Higher Order Functions and First-Class Functions have 
different meanings.

Higher Order Functions - Functions that can receive and/or return other functions.

First-Class Functions - Functions that are treated like other common data types 
(strings, integers, etc.).

It wouldn't make much difference in your code, but I thought I should inform you of this.

Note: these terms can be different and still reflect the same meaning."""