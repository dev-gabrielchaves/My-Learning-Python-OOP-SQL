#Numbers without a dot can be considered as a "int" 
print(1, -1, 0)
#Numbers with a dot can be considered as a "float"
print(1.1, -1.1, 0.0)
#boolean type returns "true" or "false"
print(10==11)
print(10==10)
#Use the command "type" to show the type of the variable
print(type(1), type(-1), type(0))
print(type(1.1), type(-1.1), type(0.0))
print(type('Hello'), type('-1'), type('0'))
print(type(True), type(False), type(10==10), type(10==11))